                <td class="bold_black">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、总和</td>
              </tr>
              <tr>
                <td>&nbsp;</td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">大小：</span>
<ul class="no_style">
                <li>根据相应单项投注的第一球 ~ 第五球开出的球号数字总和值大于或等于 23 为总和大，小于或等于 22 为总和小。</li>
                    </ul>
                  </li>
                  <li><span class="red">单双：</span>
<ul class="no_style">
                <li>根据相应单项投注的第一球 ~ 第五球开出的球号数字总和值是双数为总和双，数字总和值是单数为总和单。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td class="bold_black">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、前三特殊玩法： 豹子 > 顺子 > 对子 > 半顺 > 杂六</td>
              </tr>
              <tr>
                <td>&nbsp;</td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">豹子：</span>
<ul class="no_style">
                <li>开奖号码的万位千位百位数字都相同。如中奖号码为：222XX、666XX、888XX...开奖号码的万位千位百位数字相同，则投注前三豹子者视为中奖，其它视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">顺子：</span>
<ul class="no_style">
                <li>开奖号码的万位千位百位数字都相连，不分顺序（数字9、0、1相连）。如中奖号码为：123XX、901XX、321XX、798XX...开奖号码的万位千位百位数字相连，则投注前三顺子者视为中奖，其它视为不中奖。 </li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">对子：</span>
<ul class="no_style">
                <li>开奖号码的万位千位百位任意两位数字相同（不包括豹子）。如中奖号码为：001XX，288XX、696XX...开奖号码的万位千位百位有两位数字相同，则投注前三对子者视为中奖，其它视为不中奖。如果开奖号码为前三豹子，则前三对子视为不中奖。 </li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">半顺：</span>
<ul class="no_style">
                <li>开奖号码的万位千位百位任意两位数字相连，不分顺序（不包括顺子、对子）。如中奖号码为：125XX、540XX、390XX、160XX...开奖号码的万位千位百位有两位数字相连，则投注前三半顺者视为中奖，其它视为不中奖。如果开奖号码为前三顺子、前三对子，则前三半顺视为不中奖。如开奖号码为：123XX、901XX、556XX、233XX...视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">杂六：</span>
<ul class="no_style">
                <li>不包括豹子、对子、顺子、半顺的所有开奖号码。如开奖号码为：157XX、268XX...开奖号码位数之间无关联性，则投注前三杂六者视为中奖，其它视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
