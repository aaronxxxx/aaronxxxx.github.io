                <td class="bold_black">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、第一球 ~ 第五球</td>
              </tr>
              <tr>
                <td>&nbsp;</td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">第一球~第五球：</span>
<ul>
                <li class="no_style">第一球、第二球、第三球、第四球、第五球：指下注的每一球特码与开出之号码其开奖顺序及开奖号码相同，视为中奖，如第一球开出号码 8，下注第一球为 8 者视为中奖，其余情形视为不中奖。</li>
                    </ul>
                  </li>
                  <li><span class="red">单双大小</span>
<ul>
                <li class="no_style">根据相应单项投注第一球 ~ 第五球开出的球号，判断胜负。</li>
                    </ul>
                  </li>
                  <li><span class="red">大小：</span>
<ul>
                <li class="no_style">根据相应单项投注的第一球 ~ 第五球开出的球号大于或等于 5 为特码大，小于或等于 4 为特码小。</li>
                    </ul>
                  </li>
                  <li><span class="red">单双：</span>
<ul>
                <li class="no_style">根据相应单项投注的第一球 ~ 第五球开出的球号为双数则为特码双，如 2、6；球号为单数则为特码单，如 1、3。</li>
                    </ul>
                  </li>
                </ul>
                  </li>
                  </ul></td>
              </tr>
