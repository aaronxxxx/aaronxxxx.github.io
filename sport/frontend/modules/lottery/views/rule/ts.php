                <td class="bold_black">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6、龙虎和特殊玩法： 龙 > 虎 > 和 （0为最小，9为最大）</td>
              </tr>
              <tr>
                <td>&nbsp;</td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">龙：</span>
<ul class="no_style">
                <li>开奖第一球（万位）的号码 大于 第五球（个位）的号码。如：第一球开出 6，第五球开出 2、第一球开出 8，第五球开出 6、第一球开出 5，第五球开出 1...开奖为龙，投注龙者视为中奖，其它视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">虎：</span>
<ul class="no_style">
                <li>开奖第一球（万位）的号码 小于 第五球（个位）的号码。如：第一球开出 2，第五球开出 6、第一球开出 6，第五球开出 8、第一球开出 1，第五球开出 5...开奖为虎，投注虎者视为中奖，其它视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
              </tr>
              <tr>
                <td><ul>
                  <li><span class="red">和：</span>
<ul class="no_style">
                <li>开奖第一球（万位）的号码 等于 第五球（个位）的号码。如：2XXX2、6XXX6、8XXX8...开奖为和，投注和者视为中奖，其它视为不中奖。</li>
                    </ul>
                  </li>
                </ul></td>
